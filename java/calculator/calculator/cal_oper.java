package calculator;

public class cal_oper {
    public int add2 (int a, int b){
        return a+b;
    }
    public int sub2 (int a, int b){
        return a-b;
    }
    public int mul2 (int a, int b){
        return a*b;
    }
    public float div2 (float a, float b){
        return a/b;
    }
}